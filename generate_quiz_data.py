import json
from pathlib import Path


BASE = Path(__file__).resolve().parent / "quiz_data"
BASE.mkdir(exist_ok=True)


def write_file(name: str, payload: dict) -> None:
    (BASE / name).write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")


def num_word(n: int) -> str:
    words = {
        0: "ноль", 1: "один", 2: "два", 3: "три", 4: "четыре", 5: "пять", 6: "шесть", 7: "семь",
        8: "восемь", 9: "девять", 10: "десять", 11: "одиннадцать", 12: "двенадцать", 13: "тринадцать",
        14: "четырнадцать", 15: "пятнадцать", 16: "шестнадцать", 17: "семнадцать", 18: "восемнадцать",
        19: "девятнадцать", 20: "двадцать", 21: "двадцать один", 22: "двадцать два", 23: "двадцать три",
        24: "двадцать четыре", 25: "двадцать пять", 26: "двадцать шесть", 27: "двадцать семь",
        28: "двадцать восемь", 29: "двадцать девять", 30: "тридцать",
    }
    return words.get(n, str(n))


def build_math() -> None:
    questions = []

    # 40 сложение
    for i in range(40):
        a = 2 + (i % 18)
        b = 1 + ((i * 3) % 10)
        s = a + b
        questions.append({"question": f"Сколько будет {a} плюс {b}?", "answers": [str(s), num_word(s)]})

    # 30 вычитание
    for i in range(30):
        a = 12 + (i % 18)
        b = 1 + ((i * 2) % 10)
        if b >= a:
            b = a - 1
        s = a - b
        questions.append({"question": f"Сколько будет {a} минус {b}?", "answers": [str(s), num_word(s)]})

    # 15 неизвестный компонент
    for i in range(15):
        a = 3 + i
        b = (i % 7) + 2
        c = a + b
        questions.append({"question": f"Найди число: {a} плюс сколько равно {c}?", "answers": [str(b), num_word(b)]})

    # 15 задач
    for i in range(15):
        a = 4 + (i % 9)
        b = 2 + ((i * 2) % 8)
        c = a + b
        questions.append({"question": f"В рюкзаке было {a} тетрадей, добавили {b}. Сколько стало?", "answers": [str(c), num_word(c)]})

    write_file(
        "math.json",
        {
            "category": "Математика",
            "aliases": ["математика", "счет", "счёт", "числа", "примеры"],
            "questions": questions,
        },
    )


def build_letters() -> None:
    questions = []
    words = [
        "малина", "самолет", "школа", "ландыш", "машина", "космос", "музей", "река", "планета", "тетрадь",
        "робот", "жираф", "пингвин", "глобус", "компас", "радуга", "акула", "дельфин", "фонарь", "мост",
        "история", "музыка", "чемодан", "арбуз", "гитара", "театр", "барабан", "календарь", "комета", "огурец",
        "библиотека", "профессор", "телескоп", "природа", "портфель", "улица", "витрина", "трамвай", "суббота", "понедельник",
    ]

    for w in words:
        questions.append({"question": f"Назови первую букву в слове {w}.", "answers": [w[0]]})
    for w in words:
        questions.append({"question": f"Назови последнюю букву в слове {w}.", "answers": [w[-1]]})

    syllables = [
        ("машина", 3), ("календарь", 3), ("музыка", 3), ("гитара", 3), ("пингвин", 2),
        ("радуга", 3), ("планета", 3), ("самолет", 3), ("телескоп", 3), ("робот", 2),
        ("природа", 3), ("барабан", 3), ("театр", 2), ("дельфин", 2), ("история", 4),
        ("фонарь", 2), ("чемодан", 3), ("тетрадь", 2), ("улица", 3), ("малина", 3),
    ]
    for w, n in syllables:
        questions.append({"question": f"Сколько слогов в слове {w}?", "answers": [str(n), num_word(n)]})

    vowels = set("аеёиоуыэюя")
    alphabet = list("абвгдеёжзийклмнопрстуфхцчшщэюя")
    for i, ch in enumerate(alphabet[:40]):
        kind = "гласная" if ch in vowels else "согласная"
        questions.append({"question": f"Вопрос {i+1}. Буква {ch} — гласная или согласная?", "answers": [kind]})

    write_file(
        "letters_words.json",
        {
            "category": "Буквы и слова",
            "aliases": ["буквы", "слова", "чтение", "русский", "алфавит"],
            "questions": questions[:100],
        },
    )


